#NO ES NECESARIO ";" PARA TERMINAR LA LINEA
#EL NOMBRE DE UNA VARIABLE NO PUEDE COINCIDIR CON LOS NOMBRES DE LOS COMANDOS ASIGNADOS, ADEMAS NO DEBERAN CONTENER 
#ESPACIOS EN BLANCO
#LAS VARIABLES AUTOMATICAMENTE RECONOCEN EL TIPO DE DATO QUE SE ASIGNA
#VARIABLES QUE ALMACENAN NUMEROS ENTEROS(int), DECIMALES(float)

#SUMA (+)
Numero_1 = 2
Numero_2 = 2
Numero_3 = Numero_1 + Numero_2
print("Resultado es: "+ str(Numero_3))

#RESTA (-)
Numero_1 = 2
Numero_2 = 1
Numero_3 = Numero_1 - Numero_2
print("Resultado es: "+ str(Numero_3))

#MULTIPLICACION (*)
Numero_1 = 2
Numero_2 = 2
Numero_3 = Numero_1 * Numero_2
print("Resultado es: "+ str(Numero_3))

#POTENCIACION (**)
Numero_1 = 2
Exponente = 5
Numero_3 = Numero_1 ** Exponente
print("Resultado es: "+ str(Numero_3))

#DIVICION (/)
Numero_1 = 4
Numero_2 = 2
Numero_3 = Numero_1 / Numero_2
print("Resultado es: "+ str(Numero_3))

#MODULO O RESTO(%)
Numero_1 = 30
Numero_2 = 8
Numero_3 = Numero_1 % Numero_2
print("Resultado es: "+ str(Numero_3))

#OBTENER EL VALOR ENTERO DE LA DIVISION
#DIVISION ENTERA(//)
Numero_1 = 4
Numero_2 = 2
Numero_3 = Numero_1 // Numero_2
print("Resultado es: "+ str(Numero_3))
